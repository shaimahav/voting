from django.db import models

# Create your models here.
class login(models.Model):
    username = models.CharField(max_length=20)
    password = models.CharField(max_length=20)

class state(models.Model):
    state=models.CharField(max_length=20)

class district(models.Model):
    stateid=models.ForeignKey(state, on_delete=models.CASCADE)
    district=models.CharField(max_length=20)

class landmark(models.Model):
    districtid=models.ForeignKey(district, on_delete=models.CASCADE)
    place=models.CharField(max_length=20)

class party(models.Model):
    partyname=models.CharField(max_length=20)

class assembly(models.Model):
    landmarkid=models.ForeignKey(landmark, on_delete=models.CASCADE)
    assemblyname=models.CharField(max_length=20)

class symbol(models.Model):
    partyid=models.ForeignKey(party, on_delete=models.CASCADE)
    symbol=models.FileField(max_length=20)
    symbolname=models.CharField(max_length=20,default="name")


class election_announcement(models.Model):
    election_name = models.CharField(max_length=20,default="asd")

    stateid = models.ForeignKey(state, on_delete=models.CASCADE)
    district = models.ForeignKey(district, on_delete=models.CASCADE)
    landmark = models.ForeignKey(landmark, on_delete=models.CASCADE)
    assembly=models.ForeignKey(assembly, on_delete=models.CASCADE)
    declarationdate=models.CharField(max_length=20)
    nominationsubmissiondate=models.CharField(max_length=20)
    nominationwithdrawdate=models.CharField(max_length=20)
    campinestartingdate=models.CharField(max_length=20)
    campineendingdate=models.CharField(max_length=20)
    electiondate=models.CharField(max_length=20)
    resultdate=models.CharField(max_length=20)

